package day5.loops;

public class ForLoop2 {
	public static void main(String args[]) { 

		for (char b1 = 'a'; b1 <= 'z'; b1++) {
			System.out.print(b1 + " ");
		}
		System.out.println("\n*****Print in CAP*********");

		for (char c1 = 'A'; c1 <= 'Z'; c1++) {
			System.out.print(c1 + " ");
		}
	
	}
}	
	
		
	